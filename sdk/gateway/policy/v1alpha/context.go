package policyv1alpha

// Body represents HTTP request or response body data
type Body struct {
	// Content is the body payload (may be nil)
	Content []byte

	// EndOfStream indicates if this is the final chunk of body data
	// true = complete body received, false = more chunks may follow
	EndOfStream bool

	// Present indicates if body data is available
	// false = body was not sent or not buffered (e.g., streaming)
	// true = body is available (Content may still be nil for empty bodies)
	Present bool
}

// JWTAuthDetails holds fields specific to JWT/OAuth2 token-based auth.
type JWTAuthDetails struct {
	// Subject is the "sub" claim from the token.
	Subject string

	// Issuer is the token issuer ("iss" claim, IdP URL).
	Issuer string

	// Audience is the intended audience ("aud" claim). Can be multiple values.
	Audience []string

	// Claims holds additional token claims that don't fit the typed fields.
	Claims map[string]string
}

