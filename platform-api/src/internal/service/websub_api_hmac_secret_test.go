// TestSlugifyHmacSecret verifies the slug conversion used to derive secret names
// from human-readable display names.
func TestSlugifyHmacSecret(t *testing.T) {
	cases := []struct {
		input string
		want  string
	}{
		{"My GitHub Secret", "my-github-secret"},
		{"  leading and trailing  ", "leading-and-trailing"},
		{"hello_world", "hello-world"},
		{"UPPER CASE", "upper-case"},
		{"already-slugified", "already-slugified"},
		{"mixed  spaces   and__underscores", "mixed--spaces---and--underscores"},
		{"special!@#chars", "specialchars"},
		{"", ""},
		{"123numeric456", "123numeric456"},
	}
	for _, tc := range cases {
		got := slugifyHmacSecret(tc.input)
		if got != tc.want {
			t.Errorf("slugifyHmacSecret(%q) = %q, want %q", tc.input, got, tc.want)
		}
	}
}

