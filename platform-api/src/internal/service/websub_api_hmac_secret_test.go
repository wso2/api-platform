// TestWebSubAPIHmacSecret_Regenerate_ReplacesValue verifies that Regenerate
// produces a new plaintext value and updates the stored ciphertext.
func TestWebSubAPIHmacSecret_Regenerate_ReplacesValue(t *testing.T) {
	websubRepo := newMockWebSubAPIRepository()
	withWebSubAPI(websubRepo)
	hmacRepo := newMockHmacSecretRepo()
	svc := buildHmacSecretService(t, websubRepo, hmacRepo)

	if _, _, err := svc.Generate("org-uuid", "test-api", "Webhook Key", "", "test-user"); err != nil {
		t.Fatalf("Generate failed: %v", err)
	}
	originalCiphertext := hmacRepo.store[hmacSecretKey("art-uuid-1", "webhook-key")].EncryptedSecret

	_, newPlaintext, err := svc.Regenerate("org-uuid", "test-api", "webhook-key", "", "test-user")
	if err != nil {
		t.Fatalf("Regenerate failed: %v", err)
	}

	if !strings.HasPrefix(newPlaintext, "whsec_") {
		t.Errorf("regenerated plaintext should start with 'whsec_', got %q", newPlaintext)
	}
	if len(newPlaintext) != 70 {
		t.Errorf("expected regenerated plaintext length 70, got %d", len(newPlaintext))
	}

	updatedCiphertext := hmacRepo.store[hmacSecretKey("art-uuid-1", "webhook-key")].EncryptedSecret
	if updatedCiphertext == originalCiphertext {
		t.Error("ciphertext should change after Regenerate")
	}
}

