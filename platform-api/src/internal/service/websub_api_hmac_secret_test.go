// TestWebSubAPIHmacSecret_Generate_AutoGenerated verifies that Generate produces
// a plaintext in the expected whsec_ format and persists the secret metadata.
func TestWebSubAPIHmacSecret_Generate_AutoGenerated(t *testing.T) {
	websubRepo := newMockWebSubAPIRepository()
	withWebSubAPI(websubRepo)
	hmacRepo := newMockHmacSecretRepo()
	svc := buildHmacSecretService(t, websubRepo, hmacRepo)

	meta, plaintext, err := svc.Generate("org-uuid", "test-api", "My GitHub Secret", "", "test-user")
	if err != nil {
		t.Fatalf("Generate failed: %v", err)
	}

	// Plaintext format: whsec_ + 64 hex chars = 70 chars.
	if !strings.HasPrefix(plaintext, "whsec_") {
		t.Errorf("plaintext should start with 'whsec_', got %q", plaintext)
	}
	if len(plaintext) != 70 {
		t.Errorf("expected plaintext length 70, got %d", len(plaintext))
	}

	// Metadata.
	if meta == nil {
		t.Fatal("expected non-nil metadata")
	}
	if meta.Handle != "my-github-secret" {
		t.Errorf("expected slugified handle 'my-github-secret', got %q", meta.Handle)
	}
	if meta.Name != "My GitHub Secret" {
		t.Errorf("expected name 'My GitHub Secret', got %q", meta.Name)
	}
	if meta.ArtifactUUID != "art-uuid-1" {
		t.Errorf("expected artifact UUID 'art-uuid-1', got %q", meta.ArtifactUUID)
	}
	if meta.Status != "active" {
		t.Errorf("expected status 'active', got %q", meta.Status)
	}

	// Secret stored in repo with correct key.
	stored := hmacRepo.store[hmacSecretKey("art-uuid-1", "my-github-secret")]
	if stored == nil {
		t.Fatal("secret not found in repo after Generate")
	}
	// Encrypted form should differ from plaintext.
	if stored.EncryptedSecret == plaintext {
		t.Error("encrypted secret must not equal plaintext")
	}
}

