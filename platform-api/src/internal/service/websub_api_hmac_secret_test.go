// testEncKey is a valid 64-hex-char (32-byte) AES-256 key for use in tests.
const testEncKey = "aabbccddeeff00112233445566778899aabbccddeeff00112233445566778899"

// ─── Mock HMAC Secret Repository ─────────────────────────────────────────────

type mockHmacSecretRepo struct {
	repository.WebSubAPIHmacSecretRepository
	store map[string]*model.WebSubAPIHmacSecret
}

func newMockHmacSecretRepo() *mockHmacSecretRepo {
	return &mockHmacSecretRepo{store: make(map[string]*model.WebSubAPIHmacSecret)}
}

func hmacSecretKey(artifactUUID, name string) string { return artifactUUID + ":" + name }

func (m *mockHmacSecretRepo) Create(s *model.WebSubAPIHmacSecret) error {
	key := hmacSecretKey(s.ArtifactUUID, s.Handle)
	if _, exists := m.store[key]; exists {
		return errors.New("UNIQUE constraint failed: websub_api_hmac_secrets.artifact_uuid, websub_api_hmac_secrets.handle")
	}
	s.CreatedAt = time.Now()
	s.UpdatedAt = time.Now()
	cp := *s
	m.store[key] = &cp
	return nil
}

func (m *mockHmacSecretRepo) GetByArtifactAndName(artifactUUID, name string) (*model.WebSubAPIHmacSecret, error) {
	s, ok := m.store[hmacSecretKey(artifactUUID, name)]
	if !ok {
		return nil, nil
	}
	cp := *s
	return &cp, nil
}

func (m *mockHmacSecretRepo) ListByArtifact(artifactUUID string) ([]*model.WebSubAPIHmacSecret, error) {
	prefix := artifactUUID + ":"
	var result []*model.WebSubAPIHmacSecret
	for k, v := range m.store {
		if strings.HasPrefix(k, prefix) {
			cp := *v
			result = append(result, &cp)
		}
	}
	return result, nil
}

func (m *mockHmacSecretRepo) Update(s *model.WebSubAPIHmacSecret) error {
	key := hmacSecretKey(s.ArtifactUUID, s.Handle)
	if _, ok := m.store[key]; !ok {
		return errors.New("not found")
	}
	cp := *s
	m.store[key] = &cp
	return nil
}

func (m *mockHmacSecretRepo) Delete(artifactUUID, name string) error {
	key := hmacSecretKey(artifactUUID, name)
	if _, ok := m.store[key]; !ok {
		return errors.New("not found")
	}
	delete(m.store, key)
	return nil
}

// ─── Helpers ─────────────────────────────────────────────────────────────────

// withWebSubAPI pre-populates the mock with one API so HMAC secret operations
// can find it by handle. The UUID is fixed so tests can predict store keys.
func withWebSubAPI(repo *mockWebSubAPIRepository) {
	repo.store["test-api"] = &model.WebSubAPI{
		UUID:             "art-uuid-1",
		Handle:           "test-api",
		OrganizationUUID: "org-uuid",
		ProjectUUID:      "proj-uuid",
	}
}

func buildHmacSecretService(t *testing.T, websubRepo *mockWebSubAPIRepository, hmacRepo *mockHmacSecretRepo) *WebSubAPIHmacSecretService {
	t.Helper()
	logger := slog.New(slog.NewTextHandler(io.Discard, nil))
	svc, err := NewWebSubAPIHmacSecretService(hmacRepo, websubRepo, nil, nil, testEncKey, logger)
	if err != nil {
		t.Fatalf("NewWebSubAPIHmacSecretService: %v", err)
	}
	return svc
}

