// TestWebSubAPIHmacSecret_DecryptSecret verifies the round-trip: a generated
// secret can be decrypted back to the original plaintext via DecryptSecret.
func TestWebSubAPIHmacSecret_DecryptSecret(t *testing.T) {
	websubRepo := newMockWebSubAPIRepository()
	withWebSubAPI(websubRepo)
	hmacRepo := newMockHmacSecretRepo()
	svc := buildHmacSecretService(t, websubRepo, hmacRepo)

	_, plaintext, err := svc.Generate("org-uuid", "test-api", "Round Trip", "", "test-user")
	if err != nil {
		t.Fatalf("Generate failed: %v", err)
	}

	stored := hmacRepo.store[hmacSecretKey("art-uuid-1", "round-trip")]
	if stored == nil {
		t.Fatal("secret not in repo after Generate")
	}

	decrypted, err := svc.DecryptSecret(stored)
	if err != nil {
		t.Fatalf("DecryptSecret failed: %v", err)
	}
	if decrypted != plaintext {
		t.Errorf("DecryptSecret round-trip: want %q, got %q", plaintext, decrypted)
	}
}
