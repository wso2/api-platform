package main

import (
	"flag"
	"fmt"
	"log"
	"net"
	"os"
	"os/signal"
	"syscall"

	extprocv3 "github.com/envoyproxy/go-control-plane/envoy/service/ext_proc/v3"
	"google.golang.org/grpc"

	"github.com/yourorg/policy-engine/kernel"
	"github.com/yourorg/policy-engine/pkg/cel"
	"github.com/yourorg/policy-engine/worker/core"
)

// T091: Command-line flags implementation
var (
	extprocPort = flag.Int("extproc-port", 9001, "Port for ext_proc gRPC server")
	xdsPort     = flag.Int("xds-port", 9002, "Port for xDS policy discovery server (future)")
	configFile  = flag.String("config-file", "configs/policy-chains.yaml", "Path to policy chain configuration file")
)

// T090: Main entry point with gRPC server initialization
func main() {
	flag.Parse()

	log.Printf("Policy Engine starting...")
	log.Printf("ext_proc port: %d", *extprocPort)
	log.Printf("config file: %s", *configFile)

	// Initialize components
	// T093: Wire Kernel and Core components
	k := kernel.NewKernel()
	registry := core.GetRegistry()

	// Initialize CEL evaluator
	celEvaluator, err := cel.NewCELEvaluator()
	if err != nil {
		log.Fatalf("Failed to create CEL evaluator: %v", err)
	}

	// Initialize core execution engine
	coreEngine := core.NewCore(registry, celEvaluator)

	// Register sample policies from policy directory
	// NOTE: In production, this would be auto-generated by the Builder
	// For now, we load policy definitions from YAML files but skip implementations
	// since they need to be compiled in via the Builder
	log.Printf("Policy registration would happen here via Builder-generated code")

	// Load policy definitions from directory
	if err := registry.RegisterFromDirectory("policies"); err != nil {
		log.Printf("Warning: Failed to register policies from directory: %v", err)
		log.Printf("This is expected if policies haven't been compiled via Builder")
	}

	// Load policy chain configuration from file
	configLoader := kernel.NewConfigLoader(k, registry, coreEngine)
	if err := configLoader.LoadFromFile(*configFile); err != nil {
		log.Fatalf("Failed to load configuration: %v", err)
	}

	// Create ext_proc gRPC server
	extprocServer := kernel.NewExternalProcessorServer(k, coreEngine)

	// Start gRPC server
	lis, err := net.Listen("tcp", fmt.Sprintf(":%d", *extprocPort))
	if err != nil {
		log.Fatalf("Failed to listen on port %d: %v", *extprocPort, err)
	}

	grpcServer := grpc.NewServer()

	// Register ext_proc service
	extprocv3.RegisterExternalProcessorServer(grpcServer, extprocServer)

	log.Printf("Policy Engine listening on port %d", *extprocPort)

	// T092: Graceful shutdown handling
	// Setup signal handling for graceful shutdown
	sigChan := make(chan os.Signal, 1)
	signal.Notify(sigChan, syscall.SIGINT, syscall.SIGTERM)

	// Start server in goroutine
	go func() {
		if err := grpcServer.Serve(lis); err != nil {
			log.Fatalf("Failed to serve: %v", err)
		}
	}()

	// Wait for shutdown signal
	sig := <-sigChan
	log.Printf("Received signal %v, shutting down gracefully...", sig)

	// Graceful shutdown
	grpcServer.GracefulStop()
	log.Printf("Policy Engine shut down successfully")
}
